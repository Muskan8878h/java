public class first{
    public static void main(String[] args){
        int a=10;
        int b=10;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(b--);
        System.out.println(--b);
        System.out.println(a+=3);
        System.out.println(a-=2);
        System.out.println(a*=4);
        System.out.println(a/=3);
        System.out.println(a%=2);
    }
}